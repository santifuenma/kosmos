/* Carita SVG para cada estado emocional */
export function EmotionFaceIcon({ state, className }: { state: string; className?: string }) {
  const svg = {
    width: 20, height: 20,
    viewBox: '0 0 24 24',
    fill: 'none',
    stroke: 'currentColor',
    strokeWidth: 1.8,
    strokeLinecap: 'round' as const,
    strokeLinejoin: 'round' as const,
    className,
  }

  if (state === 'CONFIDENT') return (
    <svg {...svg}>
      <circle cx="12" cy="12" r="9" />
      <circle cx="9" cy="10" r="1" fill="currentColor" stroke="none" />
      <circle cx="15" cy="10" r="1" fill="currentColor" stroke="none" />
      <path d="M8 14 Q12 18 16 14" />
    </svg>
  )
  if (state === 'ANXIOUS') return (
    <svg {...svg}>
      <circle cx="12" cy="12" r="9" />
      <circle cx="9" cy="10" r="1" fill="currentColor" stroke="none" />
      <circle cx="15" cy="10" r="1" fill="currentColor" stroke="none" />
      <path d="M8.5 15.5 Q10 13 12 15.5 Q14 18 15.5 15.5" />
    </svg>
  )
  if (state === 'FRUSTRATED') return (
    <svg {...svg}>
      <circle cx="12" cy="12" r="9" />
      <circle cx="9" cy="10" r="1" fill="currentColor" stroke="none" />
      <circle cx="15" cy="10" r="1" fill="currentColor" stroke="none" />
      <path d="M8 16 Q12 12.5 16 16" />
    </svg>
  )
  if (state === 'TIRED') return (
    <svg {...svg}>
      <circle cx="12" cy="12" r="9" />
      <line x1="8" y1="10" x2="10" y2="10" />
      <line x1="14" y1="10" x2="16" y2="10" />
      <line x1="8.5" y1="15" x2="15.5" y2="15" />
    </svg>
  )
  /* NEUTRAL (default) */
  return (
    <svg {...svg}>
      <circle cx="12" cy="12" r="9" />
      <circle cx="9" cy="10" r="1" fill="currentColor" stroke="none" />
      <circle cx="15" cy="10" r="1" fill="currentColor" stroke="none" />
      <line x1="8.5" y1="15" x2="15.5" y2="15" />
    </svg>
  )
}
